using System;

namespace VariousTypes
{
    class Program
    {
        static void Main(string[] args)
        {
            short a = 1;
            int b = 2;
            long c = 3L;
            ushort d = 4;
            uint e = 5U;
            ulong f = 6UL;
            Console.WriteLine("Hello, World!");
            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
        }
    }
}
